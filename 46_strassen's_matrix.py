def strassen(A, B):
    m1 = (A[0][0] + A[1][1]) * (B[0][0] + B[1][1])
    m2 = (A[1][0] + A[1][1]) * B[0][0]
    m3 = A[0][0] * (B[0][1] - B[1][1])
    m4 = A[1][1] * (B[1][0] - B[0][0])
    m5 = (A[0][0] + A[0][1]) * B[1][1]
    m6 = (A[1][0] - A[0][0]) * (B[0][0] + B[0][1])
    m7 = (A[0][1] - A[1][1]) * (B[1][0] + B[1][1])
    c11 = m1 + m4 - m5 + m7
    c12 = m3 + m5
    c21 = m2 + m4
    c22 = m1 - m2 + m3 + m6
    return [[c11, c12], [c21, c22]]
